    public function login(Request $request){
        // return $request;
      $request->validate([
        'email'=>"required|email",
        'password'=>"required|min:4"
      ]);

    $auth = Auth("admins")->attempt(['email'=>$request->email , 'password'=>$request->password]);
    if($auth)
    return redirect('admin/index');  
    else
    return redirect()->back();
    }

